add_filter( 'image_send_to_editor',  __NAMESPACE__ . '\\remove_width_attribute', 10 );

function remove_width_attribute( $html ) {
   $html = preg_replace( '/(width|height)="\d*"\s/', "", $html );
   return $html;
}
